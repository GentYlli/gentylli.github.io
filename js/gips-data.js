// Function to generate product HTML with current language
function generateProductHTML(product) {
  const lang = currentLang || 'sq'; // Get current language from translations.js
  const name = product[`name_${lang}`] || product.name_sq;
  const description = product[`description_${lang}`] || product.description_sq;
  const specs = product[`specs_${lang}`] || product.specs_sq || [];
  
  const specsHTML = (specs && specs.length > 0)
    ? `<br><ul>${specs.map(spec => `<li>${spec}</li>`).join('')}</ul>`
    : '';
    
  const whatsappText = encodeURIComponent(`Përshëndetje! Dua të pyes për ${name}`);
  const contactBtnText = translations[lang]?.contact_for_price || "Kontakto për Çmim";
  
  return `
    <div class="produkt">
      <div class="img-container">
        <div class="img-box">
          <span><img src="${product.image}" onclick="openLightbox('${product.image}')" alt="${name}"></span>
        </div>
      </div>
      <div class="produkt-details">
        <div class="produkt-name">
          <strong><h1>${name}</h1></strong>
        </div>
        <div class="produkt-pershkrim">
          <p>${description}</p>
          ${specsHTML}
        </div>
        <div class="produkt-contact">
          <a href="https://wa.me/355695731993?text=${whatsappText}" class="contact-btn" target="_blank" rel="noopener noreferrer">
            <i class="fab fa-whatsapp"></i> ${contactBtnText}
          </a>
        </div>
      </div>
    </div>
  `;
}

// Function to load products when page loads
function loadProducts() {
  const profileContainer = document.getElementById('profile');
  if (profileContainer && productsData.profile) {
    profileContainer.innerHTML = productsData.profile.map(product => generateProductHTML(product)).join('');
  }
  
  const pllakaContainer = document.getElementById('pllaka');
  if (pllakaContainer && productsData.pllaka) {
    pllakaContainer.innerHTML = productsData.pllaka.map(product => generateProductHTML(product)).join('');
  }

  const stukoContainer = document.getElementById('stuko');
  if (stukoContainer && productsData.stuko) {
    stukoContainer.innerHTML = productsData.stuko.map(product => generateProductHTML(product)).join('');
  }

  const leshContainer = document.getElementById('lesh');
  if (leshContainer && productsData.lesh) {
    leshContainer.innerHTML = productsData.lesh.map(product => generateProductHTML(product)).join('');
  }

  const aksesoreContainer = document.getElementById('aksesore');
  if (aksesoreContainer && productsData.aksesore) {
    aksesoreContainer.innerHTML = productsData.aksesore.map(product => generateProductHTML(product)).join('');
  }
}

// Reload products when language changes
function reloadProducts() {
  loadProducts();
}

