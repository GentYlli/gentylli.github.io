// Function to generate product HTML
function generateProductHTML(product) {
  const specsHTML = (product.specs && product.specs.length > 0)
    ? `<br><ul>${product.specs.map(spec => `<li>${spec}</li>`).join('')}</ul>`
    : '';
    
  const whatsappText = encodeURIComponent(`Përshëndetje! Dua të pyes për ${product.name}`);
  
  return `
    <div class="produkt">
      <div class="img-container">
        <div class="img-box">
          <span><img src="${product.image}" onclick="openLightbox('${product.image}')" alt="${product.name}"></span>
        </div>
      </div>
      <div class="produkt-details">
        <div class="produkt-name">
          <strong><h1>${product.name}</h1></strong>
        </div>
        <div class="produkt-pershkrim">
          <p>${product.description}</p>
          ${specsHTML}
        </div>
        <div class="produkt-contact">
          <a href="https://wa.me/355XXXXXXXXX?text=${whatsappText}" class="contact-btn" target="_blank" rel="noopener noreferrer">
            <i class="fab fa-whatsapp"></i> Kontakto për Çmim
          </a>
        </div>
      </div>
    </div>
  `;
}

// Function to load products when page loads
function loadProducts() {
  // Load Profile products
  const profileContainer = document.getElementById('profile');
  if (profileContainer && productsData.profile) {
    profileContainer.innerHTML = productsData.profile
      .map(product => generateProductHTML(product))
      .join('');
  }
  
  // Load Pllaka products
  const pllakaContainer = document.getElementById('pllaka');
  if (pllakaContainer && productsData.pllaka) {
    pllakaContainer.innerHTML = productsData.pllaka
      .map(product => generateProductHTML(product))
      .join('');
  }

    // Load Stuko products
    const stukoContainer = document.getElementById('stuko');
    if (stukoContainer && productsData.stuko) {
      stukoContainer.innerHTML = productsData.stuko
        .map(product => generateProductHTML(product))
        .join('');
    }
  
    // Load Lesh products
    const leshContainer = document.getElementById('lesh');
    if (leshContainer && productsData.lesh) {
      leshContainer.innerHTML = productsData.lesh
        .map(product => generateProductHTML(product))
        .join('');
    }

    //Load Aksesorë products
    const aksesoreContainer = document.getElementById('aksesore');
    if (aksesoreContainer && productsData.aksesore) {
      aksesoreContainer.innerHTML = productsData.aksesore
        .map(product => generateProductHTML(product))
        .join('');
    }
}

// Run when DOM is loaded
document.addEventListener('DOMContentLoaded', loadProducts);
