// Get current language from localStorage or default to Albanian
let currentLang = localStorage.getItem('language') || 'sq';

// Function to switch language
function toggleLanguage() {
  currentLang = currentLang === 'sq' ? 'en' : 'sq';
  localStorage.setItem('language', currentLang);
  updatePageLanguage();
  
  // Update button text
  const langBtn = document.getElementById('current-lang');
  if (langBtn) {
    langBtn.textContent = currentLang === 'sq' ? 'EN' : 'SQ';
  }
  
  // Trigger event for product reload
  window.dispatchEvent(new Event('languageChanged'));
}

// Function to update all translatable elements
function updatePageLanguage() {
  // Update elements with data-translate
  document.querySelectorAll('[data-translate]').forEach(element => {
    const key = element.getAttribute('data-translate');
    if (translations[currentLang] && translations[currentLang][key]) {
      // Use innerHTML instead of textContent to render the <i> tag
      element.innerHTML = translations[currentLang][key];
    }
  });

  // Update placeholders (Keep using .placeholder here)
  document.querySelectorAll('[data-translate-placeholder]').forEach(element => {
    const key = element.getAttribute('data-translate-placeholder');
    if (translations[currentLang] && translations[currentLang][key]) {
      element.placeholder = translations[currentLang][key];
    }
  });

  document.documentElement.lang = currentLang;
}

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
  updatePageLanguage();
  const langBtn = document.getElementById('current-lang');
  if (langBtn) {
    langBtn.textContent = currentLang === 'sq' ? 'EN' : 'SQ';
  }
});